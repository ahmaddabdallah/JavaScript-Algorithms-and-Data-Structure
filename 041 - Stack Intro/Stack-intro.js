/*
    --------------------------
    -------   Stack   --------
    --------------------------

    A stack is a linear data structure that follows the Last In First Out (LIFO) principle.
    This means that the last element added to the stack will be the first one to be removed.

    Think of a stack like a pile of plates:
    - You can only add (push) or remove (pop) plates from the top of the pile
    - The last plate placed on top is the first one to be removed

    Common Stack Operations:
    - push(element): Adds an element to the top of the stack
    - pop(): Removes and returns the top element from the stack
    - peek() or top(): Returns the top element without removing it
    - isEmpty(): Returns true if the stack is empty, false otherwise
    - size(): Returns the number of elements in the stack

    Stacks are used in various applications such as:
    - Function call management (call stack)
    - Undo/Redo operations in applications
    - Expression evaluation and syntax parsing
    - Backtracking algorithms
*/
